    <h2 style="margin:0;">Čemu je namenjen ta program?</h2>
    <p>Ta program je namenjen učenju nemščine, predvsem ponavljanju in učenju nepravilnih stvari, kot so predlogi in množina pri samostalnikih, 
      nekateri časi in druge nepravilnosti pri glagolih ter na sploh učenje novih nemških besed. Kasneje bodo mogoče dodani tudi drugi jeziki, 
      zbiram pa tudi predloge, vendar ne zagotavljam, da bodo dodani, obstaja pa večja verjetnost, če več ljudi pošlje predlog za nek jezik.</p>

