    <h2 style="margin:0;">Ali se je potrebno v program prijaviti?</h2>
    <p>Zaenkrat je za vse funkcije programa potrebna prijava, vendar bo v prihodnosti mogoče možno tudi učenje brez prijave.</p>

    <div class="big-break"></div>

    <h2 style="margin:0;">Prijava</h2>
    <p>Če še niste registrirani, se lahko registrirate zelo enostavno, samo kliknete na gumb Registracija, vpišete svoje uporabniško ime in geslo, 
        ki si ga izmislite, ob vsaki naslednji prijavi pa samo ponovno vpišete svoje uporabniško ime in geslo.  
    </p>

